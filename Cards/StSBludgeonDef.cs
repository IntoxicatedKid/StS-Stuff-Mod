    }
    [EntityLogic(typeof(StSBludgeonDef))]
    public sealed class StSBludgeon : Card
    {
        [HarmonyPatch(typeof(Weak), nameof(Weak.OnDamageDealing))]
        class Weak_OnDamageDealing_Patch
        {
            static bool Prefix(StatusEffect __instance, DamageDealingEventArgs args)
            {
                if (args.ActionSource.Id == "StSBludgeon")
                {
                    return false;
                }
                return true;
            }
        }
    }
