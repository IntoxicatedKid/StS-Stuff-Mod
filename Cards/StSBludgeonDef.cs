               DebugLevel: 0,
               Revealable: false,
               IsPooled: true,
               HideMesuem: false,
               IsUpgradable: true,
               Rarity: Rarity.Rare,
               Type: CardType.Attack,
               TargetType: TargetType.SingleEnemy,
               Colors: new List<ManaColor>() { ManaColor.Red },
               IsXCost: false,
               Cost: new ManaGroup() { Any = 3, Red = 2 },
               UpgradedCost: new ManaGroup() { Any = 3, Red = 2 },
               MoneyCost: null,
               Damage: 60,
               UpgradedDamage: 70,
               Block: null,
               UpgradedBlock: null,
               Shield: null,
